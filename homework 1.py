# Նկարագրեք հետևյալ ֆունկցիայի աշխատանքը

def a_plus_abs_b(a, b):
    if b > 0:
        return a + b
    else:
        return a - b


# Մեր ֆունկցիան անում է հետևյալ գործողությունները․եթե b թիվը մեծ ե 0-ից (if b > 0), վերադարձնի a-ի և b-ի absolute արժեքի գումարը(return a + b),
# իսկ մնացած բոլոր դեպքերում (else b > 0) վերադարձնում a - b(return a - b)

def a_plus_abs_b(a, b):
    if b > 0:
        return a + b
    return a - b

