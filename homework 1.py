# Ստեղծեք ֆունկցիա,որը որպես արգումենտ ստանում է երեք թվեր և վերադարձնում դրանցից երկու մեծագույնների քառակուսիների գումարը

def sum_of_two_squares(a, b, c):
    if a >= b >= c:
        x = a
        y = b
    elif b >= c >= a:
        x = b
        y = c
    else:
        x = a
        y = c

    return (x**2) + (y**2)


print(sum_of_two_squares(5, 4, 1))

